        <h3><b>Gestão Democrática e Projeto Político Pedagógico:</b> entre a ilusão e a realidade no contidiano de uma escola pública</h3>
        
        <p>Giselle Amaral</p>

        <p><b>Sinópse</b></p>

        <p>Esta obra, Gestão democrática e Projeto Político Pedagógico: entre a ilusão e a realidade no cotidiano de uma escola pública, lança um olhar sobre os estudos e as análises a respeito da gestão democrática, presente nos documentos legais e nos discursos oficiais de uma instituição de ensino pública, tão defendida no papel, mas pouco concretizada na escola. Essas diversas incongruências são desvendadas com base numa pesquisa realizada com a comunidade escolar, evidenciando as contradições entre o discurso oficial e as práticas vivenciadas pelos sujeitos da escola. É importante destacar que a consolidação de uma gestão democrática ainda é uma luta que se faz presente nas escolas públicas brasileiras. De maneira clara e objetiva, a autora faz uma retrospectiva histórica a respeito dos estudos da administração escolar no país, da gestão da educação brasileira, seus princípios e suas concepções, do percurso entre a adoção da concepção da administração e da gestão democrática com base nos documentos legais. Ainda, aborda interfaces entre o gerencialismo e a gestão democrática, bem como discute os modelos de planejamento e organização do trabalho pedagógico, visando trazer uma reflexão acerca do seguinte questionamento: “Projeto Político Pedagógico: uma construção coletiva?”.</p>

        <p><b>Sobre a autora</b></p>

        <p>Mais do que sistematizar e disponibilizar uma obra na área da Educação, espera-se suscitar questionamentos, inquietações, romper bloqueios e apontar caminhos, sem a intenção de esgotar as discussões acerca dessa temática, pois no domínio científico, toda conclusão é provisória, tendo em vista que a realidade é dinâmica, sujeita a mudanças, alterações. Logo, nesta obra, propõe-se levantar questões, em direção a uma prática pedagógica mais democrática, comprometida com a transformação social, buscando ouvir os sujeitos, seus posicionamentos e anseios, indo além do que está explicitado nos documentos oficiais. Esse é o maior desafio.</p>

